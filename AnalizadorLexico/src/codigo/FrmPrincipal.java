    private void analizarLexico() throws IOException{
        int cont = 1;
        
        String expr = (String) txtResultado.getText();
        Lexer lexer = new Lexer(new StringReader(expr));
        String resultado = "LINEA " + cont + "\t\tSIMBOLO\n";
        while (true) {
            Tokens token = lexer.yylex();
            if (token == null) {
                txtAnalizarLex.setText(resultado);
                return;
            }
            switch (token) {
                case Linea:
                    cont++;
                    resultado += "LINEA " + cont + "\n";
                    break;
                case Comillas:
                    resultado += "  <Comillas>\t\t" + lexer.lexeme + "\n";
                    break;
                case Negacion:
                    resultado += "  <Negacion>\t\t" + lexer.lexeme + "\n";
                    break;
                case And:
                    resultado += "  <Conector y>\t" + lexer.lexeme + "\n";
                    break;
                case Or:
                    resultado += "  <Conector o>\t" + lexer.lexeme + "\n";
                    break;
                case Sientonces:
                    resultado += "  <Conector si entonces →>\t" + lexer.lexeme + "\n";
                    break;
                case Siysolosi:
                    resultado += "  <Conector si y solo si ↔ >\t" + lexer.lexeme + "\n";
                    break;
                case Proposicion:
                    resultado += "  <Proposicion>\t\t" + lexer.lexeme + "\n";
                    break;
                
                case Parentesis_a:
                    resultado += "  <Parentesis de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case Parentesis_c:
                    resultado += "  <Parentesis de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case ERROR:
                    resultado += "  <Simbolo no definido>\n";
                    break;
                default:
                    resultado += "  < " + lexer.lexeme + " >\n";
                    break;
            }
        }
    }
